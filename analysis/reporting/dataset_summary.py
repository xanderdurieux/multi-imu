"""Compact thesis-ready dataset summary.

Reads ``features_fused.csv`` (or any feature table) and produces a complete
audit trail of the dataset — recordings, sections, windows, labels, quality
tiers, exclusions, and per-scheme class counts — without duplicating the
figures already generated by :mod:`reporting.dataset_stats`.

Outputs
-------
tables/section_inventory.csv
    One row per section: window counts, label coverage, duration, quality.
tables/label_scheme_counts.csv
    Class counts for the three labelling schemes (fine / coarse / binary),
    stacked in long format with a ``scheme`` column.
tables/exclusion_funnel.csv
    Step-by-step window exclusion audit trail from raw to training-ready.
dataset_summary.json
    All key numbers in one compact machine-readable file.
figures/dataset/label_scheme_comparison.png
    Three-panel bar chart comparing fine-grained, coarse, and binary counts.
figures/dataset/exclusion_funnel.png
    Horizontal waterfall showing retained and excluded windows per step.

Public API
----------
::

    from reporting.dataset_summary import run_dataset_summary
    summary = run_dataset_summary(features_path, output_dir=output_dir)
"""

