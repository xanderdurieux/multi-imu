def plot_label_scheme_comparison(
    scheme_counts: pd.DataFrame,
    output_path: Path,
) -> Path:
    """Three-panel figure comparing fine-grained, coarse, and binary label counts.

    Each panel is a horizontal bar chart sorted by count (largest at top).
    The schemes share no axis so their y-scales are independent — this lets
    all three sit in a column without the fine-grained panel dominating.
    """
    schemes = ["fine", "coarse", "binary"]
    scheme_labels = {"fine": "Fine-grained", "coarse": "Coarse (4-class)", "binary": "Binary"}

    # Filter out unlabeled from display
    plot_df = scheme_counts[scheme_counts["class"] != "unlabeled"].copy()

    fig, axes = plt.subplots(
        len(schemes), 1,
        figsize=(8, sum(
            max(2.5, 0.45 * len(plot_df[plot_df["scheme"] == s]) + 1.0)
            for s in schemes
        )),
        constrained_layout=True,
    )
    if len(schemes) == 1:
        axes = [axes]

    for ax, scheme in zip(axes, schemes):
        sub = plot_df[plot_df["scheme"] == scheme].sort_values("n_windows")
        if sub.empty:
            ax.set_visible(False)
            continue

        labels = sub["class"].tolist()
        values = sub["n_windows"].tolist()
        colors = [_class_color(lbl) for lbl in labels]

        bars = ax.barh(labels, values, color=colors, edgecolor="white", linewidth=0.4)

        max_val = max(values) if values else 1
        for bar, val in zip(bars, values):
            ax.text(
                bar.get_width() + max_val * 0.01,
                bar.get_y() + bar.get_height() / 2,
                str(val),
                va="center", ha="left", fontsize=8,
            )

        total = int(sub["n_windows"].sum())
        ax.set_title(f"{scheme_labels.get(scheme, scheme)}  (n = {total:,})", fontsize=10)
        ax.set_xlabel("Windows", fontsize=8)
        ax.set_xlim(0, max_val * 1.18)
        ax.grid(axis="x", alpha=0.3, lw=0.5)
        ax.spines["top"].set_visible(False)
        ax.spines["right"].set_visible(False)
        ax.tick_params(labelsize=8)

    fig.suptitle("Dataset composition by labelling scheme", fontsize=11, y=1.01)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(output_path, dpi=_DPI, bbox_inches="tight")
    plt.close(fig)
    log.info("Wrote label scheme comparison → %s", project_relative_path(output_path))
    return output_path


def plot_exclusion_funnel(
    funnel: pd.DataFrame,
    output_path: Path,
) -> Path:
    """Horizontal stacked bar chart showing windows retained and excluded per step.

    Each row is one funnel step.  The green segment = retained windows;
    the red segment = windows excluded at that step.
    """
    steps = funnel["step"].tolist()
    retained = funnel["n_windows"].tolist()
    excluded = funnel["n_excluded"].tolist()
    total = retained[0] if retained else 1

    fig, ax = plt.subplots(figsize=(9, max(2.5, 0.6 * len(steps) + 1.5)))

    y = range(len(steps))

    bars_ret = ax.barh(
        list(y), retained,
        color=_FUNNEL_RETAINED, edgecolor="white", linewidth=0.5,
        label="Retained",
    )
    ax.barh(
        list(y), excluded, left=retained,
        color=_FUNNEL_EXCLUDED, edgecolor="white", linewidth=0.5,
        label="Excluded at this step",
    )

    # Annotate retained count inside the green bar
    for i, (ret, exc) in enumerate(zip(retained, excluded)):
        # Retained count
        ax.text(
            ret / 2, i, f"{ret:,}",
            va="center", ha="center", fontsize=8, color="white" if ret > total * 0.1 else "black",
            fontweight="bold",
        )
        # Excluded count (only if non-zero)
        if exc > 0:
            ax.text(
                ret + exc / 2, i, f"−{exc:,}",
                va="center", ha="center", fontsize=7, color="#B71C1C",
            )

    step_labels = [
        f"{r['step']}  [{r['pct_retained']:.0f}%]"
        for _, r in funnel.iterrows()
    ]
    ax.set_yticks(list(y))
    ax.set_yticklabels(step_labels, fontsize=9)
    ax.set_xlabel("Windows", fontsize=9)
    ax.set_xlim(0, total * 1.05)
    ax.set_title("Window exclusion funnel", fontsize=11)
    ax.legend(loc="lower right", fontsize=8, framealpha=0.85)
    ax.grid(axis="x", alpha=0.3, lw=0.5)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)

    # Annotate reason on right margin
    for i, (_, row) in enumerate(funnel.iterrows()):
        reason = str(row["reason"])
        if row["n_excluded"] > 0:
            ax.text(
                total * 1.01, i, reason,
                va="center", ha="left", fontsize=6.5, color="#757575",
                clip_on=False,
            )

    fig.tight_layout()
    output_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(output_path, dpi=_DPI, bbox_inches="tight")
    plt.close(fig)
    log.info("Wrote exclusion funnel → %s", project_relative_path(output_path))
    return output_path


