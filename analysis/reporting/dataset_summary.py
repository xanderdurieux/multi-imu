def run_dataset_summary(
    features_path: Path | str,
    *,
    output_dir: Path | str,
    min_quality: str = "marginal",
    no_plots: bool = False,
) -> dict:
    """Generate all dataset summary artefacts from a feature table.

    Parameters
    ----------
    features_path:
        Path to the fused feature CSV (typically ``data/exports/features_fused.csv``).
    output_dir:
        Root output directory.  Tables go to ``<output_dir>/tables/``,
        figures to ``<output_dir>/figures/dataset/``, and the JSON summary
        directly under ``<output_dir>/``.
    min_quality:
        Minimum quality tier used in the exclusion funnel (mirrors the
        ``min_quality_label`` setting in ``WorkflowConfig``).
    no_plots:
        Skip figure generation (useful for headless CI runs).

    Returns
    -------
    The compact summary dict (same content as ``dataset_summary.json``).
    """
    features_path = Path(features_path)
    output_dir = Path(output_dir)
    tables_dir = output_dir / "tables"
    figures_dir = output_dir / "figures" / "dataset"
    tables_dir.mkdir(parents=True, exist_ok=True)
    figures_dir.mkdir(parents=True, exist_ok=True)

    if not features_path.exists():
        raise FileNotFoundError(
            f"Feature table not found: {features_path}. "
            "Run the 'exports' stage first."
        )

    log.info("Loading features from %s", project_relative_path(features_path))
    df_raw = read_csv(features_path)
    log.info("Loaded %d rows, %d columns", len(df_raw), len(df_raw.columns))

    # Ensure derived label columns are present (back-compat with pre-redesign CSVs)
    df = _ensure_derived_label_cols(df_raw)

    # ------------------------------------------------------------------
    # 1. Section inventory
    # ------------------------------------------------------------------
    log.info("Building section inventory …")
    section_inv = _build_section_inventory(df)
    inv_path = tables_dir / "section_inventory.csv"
    write_csv(section_inv, inv_path)
    log.info(
        "Wrote section inventory (%d sections) → %s",
        len(section_inv),
        project_relative_path(inv_path),
    )

    # ------------------------------------------------------------------
    # 2. Label scheme counts
    # ------------------------------------------------------------------
    log.info("Building label scheme counts …")
    scheme_counts = _build_label_scheme_counts(df)
    counts_path = tables_dir / "label_scheme_counts.csv"
    write_csv(scheme_counts, counts_path)
    log.info("Wrote label scheme counts → %s", project_relative_path(counts_path))

    # ------------------------------------------------------------------
    # 3. Exclusion funnel
    # ------------------------------------------------------------------
    log.info("Building exclusion funnel …")
    funnel = _build_exclusion_funnel(df, min_quality=min_quality)
    funnel_path = tables_dir / "exclusion_funnel.csv"
    write_csv(funnel, funnel_path)
    log.info("Wrote exclusion funnel → %s", project_relative_path(funnel_path))

    # ------------------------------------------------------------------
    # 4. Compact JSON summary
    # ------------------------------------------------------------------
    summary = _build_summary_dict(
        df_raw, funnel, scheme_counts, section_inv,
        features_path=features_path,
        min_quality=min_quality,
    )
    json_path = output_dir / "dataset_summary.json"
    json_path.write_text(json.dumps(summary, indent=2, default=str), encoding="utf-8")
    log.info("Wrote dataset summary → %s", project_relative_path(json_path))

    # ------------------------------------------------------------------
    # 5. Figures
    # ------------------------------------------------------------------
    if not no_plots:
        try:
            plot_label_scheme_comparison(
                scheme_counts, figures_dir / "label_scheme_comparison.png"
            )
        except Exception as exc:
            log.warning("label_scheme_comparison figure failed: %s", exc)

        try:
            plot_exclusion_funnel(funnel, figures_dir / "exclusion_funnel.png")
        except Exception as exc:
            log.warning("exclusion_funnel figure failed: %s", exc)

    n_rec = summary["recordings"]["n"]
    n_sec = summary["sections"]["n"]
    n_win = summary["windows"]["raw"]
    log.info(
        "Dataset summary complete: %d recordings, %d sections, %d windows → %s",
        n_rec, n_sec, n_win,
        project_relative_path(output_dir),
    )
    return summary
