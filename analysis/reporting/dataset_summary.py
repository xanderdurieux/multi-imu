def _build_section_inventory(df: pd.DataFrame) -> pd.DataFrame:
    """Return one row per section with window/label/quality counts and duration.

    Derived entirely from the feature table — no disk reads required.
    """
    rows: list[dict] = []
    for section_id, grp in df.groupby("section_id", sort=True):
        recording = str(section_id).rsplit("s", 1)[0] if "s" in str(section_id) else str(section_id)

        fine = grp["scenario_label"].fillna("unlabeled").astype(str) if "scenario_label" in grp.columns else pd.Series(dtype=str)
        n_windows = len(grp)
        n_unlabeled = int((fine == "unlabeled").sum())
        n_labeled = n_windows - n_unlabeled
        fraction_labeled = round(n_labeled / n_windows, 3) if n_windows else 0.0

        duration_s = float(grp["window_duration_s"].sum()) if "window_duration_s" in grp.columns else float("nan")
        sync_conf = float(grp["sync_confidence"].mean()) if "sync_confidence" in grp.columns else float("nan")

        cal_quality = "—"
        if "calibration_quality" in grp.columns:
            mode_series = grp["calibration_quality"].dropna()
            if not mode_series.empty:
                cal_quality = str(mode_series.mode().iloc[0])

        rows.append({
            "section_id": section_id,
            "recording": recording,
            "n_windows": n_windows,
            "n_labeled": n_labeled,
            "n_unlabeled": n_unlabeled,
            "fraction_labeled": fraction_labeled,
            "duration_s": round(duration_s, 1),
            "sync_confidence_mean": round(sync_conf, 3) if np.isfinite(sync_conf) else float("nan"),
            "calibration_quality": cal_quality,
        })

    return pd.DataFrame(rows)


def _build_label_scheme_counts(df: pd.DataFrame) -> pd.DataFrame:
    """Return long-format class counts for fine / coarse / binary schemes.

    Columns: scheme, class, n_windows, fraction.
    The ``fraction`` is within-scheme (sums to 1 per scheme).
    """
    if "scenario_label" not in df.columns:
        return pd.DataFrame(columns=["scheme", "class", "n_windows", "fraction"])

    rows: list[dict] = []
    scheme_col = {
        "fine": "scenario_label",
        "activity": "scenario_label_activity",
        "coarse": "scenario_label_coarse",
        "binary": "scenario_label_binary",
    }

    for scheme, col in scheme_col.items():
        if col not in df.columns:
            continue
        counts = df[col].fillna("unlabeled").value_counts()
        total = int(counts.sum())
        for cls, n in counts.items():
            rows.append({
                "scheme": scheme,
                "class": str(cls),
                "n_windows": int(n),
                "fraction": round(int(n) / total, 4) if total else 0.0,
            })

    return pd.DataFrame(rows)


def _build_exclusion_funnel(df: pd.DataFrame, *, min_quality: str = "marginal") -> pd.DataFrame:
    """Build a step-by-step window exclusion audit trail.

    Steps
    -----
    0  raw          all rows in the feature CSV
    1  quality      after dropping rows below min_quality
    2  labeled      after dropping unlabeled windows
    3  riding       after dropping non_riding windows (binary / safety target)
    """
    steps: list[dict] = []

    def _step(name: str, n_current: int, n_prev: int, reason: str) -> None:
        excluded = n_prev - n_current
        pct_retained = round(100.0 * n_current / n_prev, 1) if n_prev else 0.0
        steps.append({
            "step": name,
            "n_windows": n_current,
            "n_excluded": excluded,
            "pct_retained": pct_retained,
            "reason": reason,
        })

    n0 = len(df)
    steps.append({"step": "raw", "n_windows": n0, "n_excluded": 0, "pct_retained": 100.0, "reason": "all windows in feature CSV"})

    # Quality filter
    if "overall_quality_label" in df.columns and min_quality in _QUALITY_ORDER:
        min_idx = _QUALITY_ORDER.index(min_quality)
        valid = set(_QUALITY_ORDER[min_idx:])
        df1 = df[df["overall_quality_label"].isin(valid)]
    else:
        df1 = df
    _step("quality", len(df1), n0, f"overall_quality_label < {min_quality!r}")

    # Unlabeled filter
    if "scenario_label" in df1.columns:
        df2 = df1[df1["scenario_label"].fillna("unlabeled") != "unlabeled"]
    else:
        df2 = df1
    _step("labeled", len(df2), len(df1), "scenario_label == 'unlabeled'")

    # Non-riding filter (applies for binary safety target)
    if "scenario_label_binary" in df2.columns:
        df3 = df2[df2["scenario_label_binary"] != "non_riding"]
    elif "scenario_label" in df2.columns:
        df3 = df2[~df2["scenario_label"].isin(_NON_RIDING_LABELS)]
    else:
        df3 = df2
    _step("riding", len(df3), len(df2), "non_riding windows excluded (binary target)")

    return pd.DataFrame(steps)


def _build_summary_dict(
    df_raw: pd.DataFrame,
    funnel: pd.DataFrame,
    scheme_counts: pd.DataFrame,
    section_inventory: pd.DataFrame,
    *,
    features_path: Path,
    min_quality: str,
) -> dict:
    """Assemble a compact JSON-serialisable summary dict."""
    funnel_idx = funnel.set_index("step")["n_windows"].to_dict()

    # Per-scheme training-ready counts and class counts
    label_counts: dict[str, dict] = {}
    training_ready: dict[str, int] = {}

    for scheme, stop_class in [("fine", None), ("coarse", None), ("binary", "non_riding")]:
        sub = scheme_counts[scheme_counts["scheme"] == scheme]
        if sub.empty:
            continue
        if stop_class:
            sub = sub[sub["class"] != stop_class]
            # also exclude unlabeled from the binary training-ready count
            sub = sub[sub["class"] != "unlabeled"]
        label_counts[scheme] = {
            r["class"]: r["n_windows"] for _, r in sub.iterrows()
        }
        training_ready[scheme] = int(sub["n_windows"].sum())

    # Quality distribution
    quality_dist: dict[str, int] = {}
    if "overall_quality_label" in df_raw.columns:
        quality_dist = df_raw["overall_quality_label"].value_counts().to_dict()
        quality_dist = {k: int(v) for k, v in quality_dist.items()}

    # Window config: infer hop from window_start_ms differences within a section
    window_s: float | None = None
    hop_s: float | None = None
    if "window_duration_s" in df_raw.columns:
        window_s = round(float(df_raw["window_duration_s"].median()), 2)
    if "section_id" in df_raw.columns and "window_start_ms" in df_raw.columns:
        first_sec = df_raw.groupby("section_id").filter(lambda g: len(g) >= 2).groupby("section_id")
        hops = []
        for _, grp in first_sec:
            diffs = grp["window_start_ms"].sort_values().diff().dropna()
            if not diffs.empty:
                hops.append(float(diffs.median()) / 1000.0)
        if hops:
            hop_s = round(float(np.median(hops)), 2)

    n_sections = int(df_raw["section_id"].nunique()) if "section_id" in df_raw.columns else 0
    recordings = sorted(
        df_raw["section_id"].str.replace(r"s\d+$", "", regex=True).unique().tolist()
    ) if "section_id" in df_raw.columns else []

    return {
        "generated_at_utc": datetime.now(timezone.utc).isoformat(),
        "source": str(features_path),
        "min_quality_filter": min_quality,
        "recordings": {"n": len(recordings), "ids": recordings},
        "sections": {"n": n_sections},
        "windows": {
            "raw": int(funnel_idx.get("raw", len(df_raw))),
            "after_quality_filter": int(funnel_idx.get("quality", 0)),
            "after_unlabeled_filter": int(funnel_idx.get("labeled", 0)),
            "after_non_riding_filter": int(funnel_idx.get("riding", 0)),
            "training_ready": training_ready,
        },
        "label_counts": label_counts,
        "quality_distribution": quality_dist,
        "window_config": {
            "window_s": window_s,
            "hop_s": hop_s,
        },
    }


